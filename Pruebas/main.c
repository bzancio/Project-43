#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(void)
{
	char	c;
	int	i;

	i = 0;
	c = 'x';
	while (i < 3)
	{
		ft_putchar(c);
		i++;
	}

}
